package com.Bronze5;

import java.util.Scanner;

public class Quiz10869 {

	public static void main(String[] args) {
		// 인트형 2개 입력받아 사칙연산출력
		Scanner sc = new Scanner(System.in);
		int a = sc.nextInt();
		int b = sc.nextInt();
		
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);
	}

}
